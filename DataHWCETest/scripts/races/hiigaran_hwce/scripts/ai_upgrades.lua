function DoUpgradeDemand_Hiigaran()

	ResearchVariantsDemandSet("hgn_interceptormaxspeedupgrade1", 5)
	ResearchVariantsDemandSet("hgn_attackbombermaxspeedupgrade1", 5)
	ResearchVariantsDemandSet("hgn_assaultcorvettehealthupgrade1", 5)
	ResearchVariantsDemandSet("hgn_assaultcorvettemaxspeedupgrade1", 5)
	ResearchVariantsDemandSet("hgn_pulsarcorvettehealthupgrade1", 2)
	ResearchVariantsDemandSet("hgn_torpedofrigatehealthupgrade1", 2)
	ResearchVariantsDemandSet("hgn_torpedofrigatemaxspeedupgrade1", 2)
	ResearchVariantsDemandSet("hgn_ioncannonfrigatehealthupgrade1", 2)
	ResearchVariantsDemandSet("hgn_ioncannonfrigatemaxspeedupgrade1", 2)
	ResearchVariantsDemandSet("hgn_assaultfrigatehealthupgrade1", 2)
	ResearchVariantsDemandSet("hgn_mothershipbuildspeedupgrade1", 2)



	if (s_militaryStrength > 10 or g_LOD == 0) then
		inc_upgrade_demand( rt_mothership, 0.5  )
		ResearchVariantsDemandAdd("hgn_mothershipbuildspeedupgrade1", 0.5)

		local numPlatforms = numActiveOfClass( s_playerIndex, ePlatform )
		if (numPlatforms > 1) then
			local numGunTurret = NumVariantSquadrons("hgn_gunturret")
			if (numGunTurret > 1) then
				inc_upgrade_demand( rt_platform.gunturret, numGunTurret*1 )
			end
			local numIonTurret = NumVariantSquadrons("hgn_ionturret")
			if (numIonTurret > 1) then
				inc_upgrade_demand( rt_platform.ionturret, numIonTurret*1 )
			end
		end
		local numCollectors = NumVariantSquadrons(kCollector)
		if (numCollectors > 0) then
			inc_upgrade_demand( rt_collector, numCollectors*0.5 )
		end
		local numRefinery = NumVariantSquadrons(kRefinery)
		if (numRefinery > 0) then
			inc_upgrade_demand( rt_refinery, numRefinery*1.5 )
		end
		local numCarrier = NumVariantSquadrons(kCarrier)
		if (numCarrier > 0) then
			inc_upgrade_demand( rt_carrier, numCarrier*1 )
			ResearchVariantsDemand("hgn_carrierbuildspeedupgrade1", numcarrier*1.25)
		end
		local numShipYards = NumVariantSquadrons(kShipyard)
		if (numShipYards > 0) then
			inc_upgrade_demand( rt_shipyard, numShipYards*1.5  )
			ResearchVariantsDemand("hgn_shipyardbuildspeedupgrade1", numshipyards*1.75)
		end
	end
	local numFighter = numActiveOfClass( s_playerIndex, eFighter )
	if (numFighter > 1) then
		local numInterceptors = NumVariantSquadrons(kInterceptor)
		if (numInterceptors > 1) then
			inc_upgrade_demand( rt_fighter.interceptor, numInterceptors*1 )
		end
		local numBombers = NumVariantSquadrons(kBomber)
		if (numBombers > 1) then
			inc_upgrade_demand( rt_fighter.bomber, numBombers*1.5 )
		end
	end
	local numBattleCruiser = NumVariantSquadrons(kBattlecruiser)
	if (numBattleCruiser > 0) then
		inc_upgrade_demand( rt_battlecruiser, numBattleCruiser*2.5  )
	end
	local numDestroyers = NumVariantSquadrons(kDestroyer)
	if (numDestroyers > 0) then
		inc_upgrade_demand( rt_destroyer, numDestroyers*2  )
	end
	local numCorvette = numActiveOfClass( s_playerIndex, eCorvette )
	if (numCorvette > 1) then
		local numAssaultCorvette = NumVariantSquadrons("hgn_assaultcorvette")
		if (numAssaultCorvette>2) then
			inc_upgrade_demand( rt_corvette.assault, numAssaultCorvette*1.25 )
		end
		local numPulsarCorvette = NumVariantSquadrons("hgn_pulsarcorvette")
		if (numPulsarCorvette>2) then
			inc_upgrade_demand( rt_corvette.pulsar, numPulsarCorvette*1.25 )
		end
	end
	local numFrigate = numActiveOfClass( s_playerIndex, eFrigate )
	if (numFrigate > 2) then
		local numTorpedoFrigate = NumVariantSquadrons("hgn_torpedofrigate")
		if (numTorpedoFrigate>2) then
			inc_upgrade_demand( rt_frigate.torpedo, numTorpedoFrigate*1.5 )
		end
		local numIonFrigate = NumVariantSquadrons("hgn_ioncannonfrigate")
		if (numIonFrigate>2) then
			inc_upgrade_demand( rt_frigate.ioncannon, numIonFrigate*1.5 )
		end
		local numAssaultFrigate = NumVariantSquadrons("hgn_assaultfrigate")
		if (numAssaultFrigate>2) then
			inc_upgrade_demand( rt_frigate.assault, numAssaultFrigate*1.5 )
		end
	end
end

function DoResearchTechDemand_Hiigaran()
	local numShipyards = NumVariantSquadrons(kShipYard) + NumVariantSquadronsQ(kShipYard)
	if (numShipyards > 0 and Util_CheckVariantResearch("hgn_battlecruiserionweapons")) then
		local battleCruiserDemand = ShipVariantDemandGet(kBattleCruiser)
		if (battleCruiserDemand > 0) then
			ResearchVariantDemandSet( "hgn_battlecruiserionweapons", battleCruiserDemand )
		end
	end
	if (Util_CheckVariantResearch("hgn_platformionweapons")) then
		local ionTurretDemand = ShipVariantDemandGet("hgn_ionturret")
		if (ionTurretDemand > 0) then
			ResearchVariantDemandSet( "hgn_platformionweapons", ionTurretDemand )
		end
	end
	if (Util_CheckVariantResearch("hgn_destroyertech") ) then		
		local destroyerDemand = ShipVariantDemandGet("hgn_destroyer")
		if (destroyerDemand > 0) then
			ResearchVariantDemandSet( "hgn_destroyertech", destroyerDemand )
		end
	end
	if (Util_CheckVariantResearch("hgn_attackbomberimprovedbombs") ) then
		local numBombers = NumVariantSquadrons(kBomber)
		if (numBombers > 2) then
			ResearchVariantDemandSet( "hgn_attackbomberimprovedbombs", numBombers )
		end
	end
	if (Util_CheckVariantResearch("hgn_improvedtorpedo") ) then
		local numTorpedoFrigs = NumVariantSquadrons("hgn_torpedofrigate")
		if (numTorpedoFrigs > 2) then
			ResearchVariantDemandSet( "hgn_improvedtorpedo", numTorpedoFrigs )
		end
	end
	if (s_militaryPop > 15 and GetRU() > 750) then
		if (Util_CheckVariantResearch("hgn_defensefieldfrigateshield") ) then
			local DFFDemand = ShipVariantDemandGet("hgn_defensefieldfrigate")
			if (DFFDemand > 0) then
				ResearchVariantDemandSet( "hgn_defensefieldfrigateshield", DFFDemand )
			end
		end
		if (Util_CheckVariantResearch("hgn_ecmprobe") ) then
			local ecmProbeDemand = ShipVariantDemandGet("hgn_ecmprobe")
			if (ecmProbeDemand>0) then
				ResearchVariantDemandSet( "hgn_ecmprobe", ecmProbeDemand )
			end
